def _default(label, count):
    """Генерирует placeholder-вопросы для заданной метки и количества."""
    return [
        {"q": f"Default Text — {label} {i}?",
         "a": ["Default A", "Default B", "Default C", "Default D"],
         "correct": (i - 1) % 4}
        for i in range(1, count + 1)
    ]

